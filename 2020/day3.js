function traverseSlope(right, down){
  let pos = 0;
  let treeCount = 0;
  for(let i=0; i<slope.length; i+=down){
    const plots = slope[i].split('');
    const currentPlot = plots[pos%31];
    if(currentPlot=='#'){ treeCount++ }
    pos += right
  }
  return treeCount;
}

